//	Exceções:
//
//		As exceções são eventos anormais que ocorrem durante a execução de um programa, mas que podem ser tratados e recuperados.
//		As exceções são usadas para lidar com situações excepcionais, como erros de entrada inválida, problemas de conexão com banco de dados, arquivos ausentes, entre outros.
//		As exceções são projetadas para serem capturadas, tratadas e possivelmente recuperadas pelo programa, permitindo a execução normal do código após o tratamento.
//		Erros:
//
//		Os erros representam condições graves e irrecuperáveis que ocorrem durante a execução de um programa e geralmente indicam falhas no sistema ou em recursos críticos.
//		Os erros são eventos não verificáveis, o que significa que o programador não é obrigado a capturá-los ou tratá-los explicitamente.
//		Os erros geralmente indicam problemas sérios, como falta de memória, falhas de hardware, violações de segurança ou erros de programação que não podem ser corrigidos durante a execução.
//		Em resumo, as exceções são usadas para situações excepcionais que podem ser tratadas pelo programa, enquanto os erros indicam condições graves que geralmente não podem ser tratadas e podem 
//		levar à finalização abrupta do programa. É uma boa prática capturar e tratar exceções, mas os erros normalmente não são tratados explicitamente, pois podem estar além do controle do programa.

